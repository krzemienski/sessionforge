/**
 * Lighthouse Best Practices audits check for:
 *   - DOCTYPE declaration
 *   - charset declaration
 *   - HTTPS for all external resources
 *   - no inline event handlers (onclick="...")
 *   - no deprecated HTML elements
 *   - no duplicate IDs
 *   - no browser errors on load
 */
describe("Lighthouse Best Practices Audits (> 90 equivalent)", () => {
  let indexHtml: string;
  let postHtml: string;

  beforeAll(async () => {
    ({ indexHtml, postHtml } = await extractPages("minimal-portfolio"));
  });

  it("DOCTYPE declaration present", () => {
    // Lighthouse: 'doctype' audit
    expect(indexHtml.trimStart()).toMatch(/^<!DOCTYPE html>/i);
  });

  it("charset is UTF-8", () => {
    // Lighthouse: 'charset' audit
    expect(indexHtml).toContain('charset="UTF-8"');
  });

  it("all external CDN resources use HTTPS", () => {
    // Lighthouse: 'is-on-https' audit
    const httpLinks = indexHtml.match(/href="http:\/\//g) ?? [];
    const httpSrcs = indexHtml.match(/src="http:\/\//g) ?? [];
    expect(httpLinks).toHaveLength(0);
    expect(httpSrcs).toHaveLength(0);
  });

  it("highlight.js CDN uses HTTPS", () => {
    expect(indexHtml).toContain("https://cdnjs.cloudflare.com");
    expect(indexHtml).not.toContain("http://cdnjs.cloudflare.com");
  });

  it("no inline event handlers (onclick, onload, etc.)", () => {
    // Lighthouse: 'uses-passive-event-listeners' / CSP best practices
    expect(indexHtml).not.toMatch(/\bon\w+\s*=/);
  });

  it("no deprecated HTML elements (font, center, marquee)", () => {
    // Lighthouse: 'uses-deprecated-apis' (HTML side)
    expect(indexHtml).not.toMatch(/<font\b/);
    expect(indexHtml).not.toMatch(/<center\b/);
    expect(indexHtml).not.toMatch(/<marquee\b/);
    expect(indexHtml).not.toMatch(/<blink\b/);
  });

  it("no duplicate element IDs", () => {
    // Lighthouse: 'duplicate-id-aria' audit
    const ids = [...indexHtml.matchAll(/\bid="([^"]+)"/g)].map((m) => m[1]);
    const unique = new Set(ids);
    expect(ids.length).toBe(unique.size);
  });

  it("all <script> tags use type module or no type (standard JS)", () => {
    // Lighthouse: no legacy script issues
    const typeMismatch = indexHtml.match(/<script[^>]+type="text\/javascript"[^>]*>/g) ?? [];
    expect(typeMismatch).toHaveLength(0);
  });

  it("external links use rel=noopener for security", () => {
    // Lighthouse: 'external-anchors-use-rel-noopener' audit
    const externalLinks = indexHtml.match(/<a[^>]+target="_blank"[^>]*>/g) ?? [];
    for (const link of externalLinks) {
      expect(link).toContain("noopener");
    }
  });

  it("post pages also satisfy all best practice criteria", () => {
    expect(postHtml.trimStart()).toMatch(/^<!DOCTYPE html>/i);
    expect(postHtml).toContain('charset="UTF-8"');
    const httpRefs = postHtml.match(/href="http:\/\//g) ?? [];
    expect(httpRefs).toHaveLength(0);
    expect(postHtml).not.toMatch(/\bon\w+\s*=/);
  });
});

