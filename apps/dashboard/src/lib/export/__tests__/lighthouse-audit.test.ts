/** Extract the rendered index and a post page from the ZIP for a given theme */
async function extractPages(themeId: "minimal-portfolio" | "technical-blog" | "changelog") {
  const buf = await buildStaticSiteZip(SAMPLE_POSTS, {
    themeId,
    themeConfig: {
      siteTitle: "Dev Portfolio",
      siteDescription: "A developer portfolio generated by SessionForge",
      authorName: "Jane Dev",
      baseUrl: "https://janedev.github.io",
    },
  });
  const zip = await JSZip.loadAsync(buf);

  const indexHtml = await zip.file("index.html")!.async("text");
  const postHtml = await zip
    .file("posts/building-a-rest-api-with-nodejs/index.html")!
    .async("text");

  return { indexHtml, postHtml };
}

